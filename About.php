<p>						INDIA Bank is India's second-largest bank with total assets of Rs. 4,062.34 billion (US$ 91 billion) at March 31, 2011 and profit after tax Rs. 51.51 billion (US$ 1,155 million) for the year ended March 31, 2011. The Bank has a network of 2,544 branches and 7,037 ATMs in India, and has a presence in 19 countries, including India. </p>

<p>						INDIA Bank offers a wide range of banking products and financial services to corporate and retail customers through a variety of delivery channels and through its specialised subsidiaries in the areas of investment banking, life and non-life insurance, venture capital and asset management. </p>

<p>						The Bank currently has subsidiaries in the United Kingdom, Russia and Canada, branches in United States, Singapore, Bahrain, Hong Kong, Sri Lanka, Qatar and Dubai International Finance Centre and representative offices in United Arab Emirates, China, South Africa, Bangladesh, Thailand, Malaysia and Indonesia. Our UK subsidiary has established branches in Belgium and Germany. </p>
